
public class Employee
{
 int EmpId;
 String EmpName;
 int EmpSal;
public int getEmpId() {
	return EmpId;
}
public void setEmpId(int empId) {
	EmpId = empId;
}
public String getEmpName() {
	return EmpName;
}
public void setEmpName(String empName) {
	EmpName = empName;
}
public int getEmpSal() {
	return EmpSal;
}
public void setEmpSal(int empSal) {
	EmpSal = empSal;
}

}
